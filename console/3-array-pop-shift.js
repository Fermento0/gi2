var frutas = ['Maçã', 'Banana', 'Laranja', 'Pêssego', 'Abacaxi',];

var ultimaFruta = frutas.pop();

console.log('Array após remover o último elemento:', frutas);
console.log('Última fruta removida:', ultimaFruta);

var primeiraFruta = frutas.shift();
console.log('Array após remover o primeiro elemento:', frutas);
console.log('Primeira fruta removida:', primeiraFruta);